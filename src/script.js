let filters = {
    'region': '',
    'bait': '',
    'spot': '',
    'time': '',
};

$(document).on('change', 'select', function(e) {
    let id = $(this).attr('id'),
        filter = id.slice(0, -1),
        value = $(this).val();
    filters[filter] = value;
    filterFishs();
});

function filterFishs() {
    let target = '';

    $('.fish').hide();
    $('#no-fish').hide();

    for (const [key, value] of Object.entries(filters)) {
        if(value) {
            target = `${target}[data-${key}="${value}"]`;
        }
    }

    $('.fish'+target).show();

    if($('.fish:visible').length <= 0) {
        $('#no-fish').show();
    }
}


