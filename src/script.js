const clocks = {
    'tyria': {
        0: 'night',
        25: 'dawn',
        30: 'day',
        140: 'dusk',
        145: 'night',

    },
    'cantha': {
        0: 'night',
        35: 'dawn',
        40: 'day',
        135: 'dusk',
        140: 'night',
    }
};

let moments = {
    'tyria': '',
    'cantha': '',
};


updateClock('tyria');
setInterval(updateClock, 20000, 'tyria');

updateClock('cantha');
setInterval(updateClock, 20000, 'cantha');

function updateClock(r) {
    const date = new Date();
    const t = parseInt((date.getUTCHours()%2).toString() + (date.getUTCMinutes()).toString());
    // const t = 135;

    for (const [k, v] of Object.entries(clocks[r])) {
        if(t >= k) {
            moments[r] = v;
        }
    }

    $('#clocks #'+r).removeClass().addClass(moments[r]).html(`<img src="img/icons/${moments[r]}.svg" />`);
}

